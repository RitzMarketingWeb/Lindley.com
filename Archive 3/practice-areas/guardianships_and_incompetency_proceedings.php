    The appointment of guardians for people who are temporarily or permanently incompetent (i.e., mentally unable to handle one or more of their daily affairs) is an area of the law often handled by lawyers practicing fiduciary litigation and that process is dictated by <a href="http://www.ncga.state.nc.us/gascripts/Statutes/StatutesTOC.pl?Chapter=0035A">Chapter 35A of the North Carolina General Statutes</a>.  Trey has handled various guardianship and incompetency matters during his career.<br /><br />
<div id="section_faq">
    <h3>My elderly father is beginning to show signs of old age and I fear more than his memory is beginning to slip.  What should I do?</h3>
    <div>You must first determine whether your father executed a Power of Attorney and/or a Health Care Power of Attorney (while competent) designating the person(s) he wanted to be responsible for his affairs in the event he became incapacitated.<br><br>
If these documents do not exist, you may consider whether to seek intervention from a court to have your father declared incompetent and ask the court to appoint you or someone(s) else to help manage your father’s affairs.</div>
    
    <h3>What is the process for instituting a guardianship proceeding?</h3>
    <div>The first step in the process is filing a <a href="http://www.nccourts.org/forms/Documents/707.pdf">Petition for Adjudication of Incompetence and Application for Appointment of Guardian or Limited Guardian</a>  specifying the reasons the person is believed to be incompetent.  These representations are made by the “Petitioner” under oath and must be notarized before filing with the clerk of court.  A <a href="http://www.nccourts.org/forms/documents/669.pdf">notice of hearing</a>  must also be filed and served on the allegedly incompetent person (who is called the “Respondent”) and the Respondent’s next of kin, as provided by law.<br>
<br>
You should consider hiring an attorney to assist you in the process and help gather the necessary documents and evidence for the hearing.  For instance, and often in contested proceedings, the clerk of court will order that a multidisciplinary evaluation (“MDE”) be conducted by trained professionals before reaching a decision.  In an effort to streamline the process, it may be prudent to have an MDE completed in advance of the hearing.</div>
    
    <h3>I live in Union County but my father lives in Wake County—am I required to file the incompetency petition in any particular place?</h3>
    <div>The incompetency petition must be filed in Wake County, the county where your father resides.  <br><br>
If the Respondent is an inpatient in a treatment facility or if his place of residence cannot be determined, it must be filed in the county where he or she is physically present.<br><br></div>
    
    <h3>How does the court or jury decide whether someone is incompetent?</h3>
    <div>A determination of incompetency requires two findings: (1) that the person lacks sufficient capacity to manage his or her affairs or to make or communicate important decisions regarding his or her person, family, or property and (2) the person’s lack of capacity is due to mental illness, mental retardation, senility, injury, or some similar cause or condition.</div>
    
    <h3>What is a Guardian of the Person?</h3>
    <div>A Guardian of the Person makes decisions about the incompetent person’s general care and well-being, such as living conditions and medical treatment.</div>
    
    <h3>What is a Guardian of the Estate?</h3>
    <div>A Guardian of the Estate makes financial decisions and manages the incompetent person’s assets.</div>
    
    <h3>What is a General Guardian?</h3>
    <div>A General Guardian is both a Guardian of the Person and a Guardian of the Estate; he or she makes decisions about the incompetent person’s care and well-being as well as manages his/her finances.</div>
    
    <h3>Who is qualified to serve as a guardian?</h3>
    <div>There aren’t any special requirements regarding guardian qualifications.  If the various parties (typically family members) cannot agree, the clerk or judge will appoint someone he or she thinks can best serve the person’s needs.  If there is cause to fear the person’s estate will be mismanaged, the court may appoint a neutral third party (such as a lawyer or accountant) to manage the assets.</div>
    
    <h3>Are guardians only appointed for those who are incompetent because of a mental condition?</h3>
    <div>No Guardians of the Estate may be appointed, for example, to protect and manage funds belonging to a minor and received through an inheritance.</div>
    
