    <a href="http://www.ncga.state.nc.us/gascripts/Statutes/StatutesTOC.pl?Chapter=0040A">Condemnation and Eminent Domain</a><br><br>
Real property is said to be condemned when a public agency, municipality, or in some cases a private entity such as a utility company, takes title for public use through the power of eminent domain.  Title passes the moment of condemnation and the new owner must pay to the clerk of court what it believes to be the fair market value of the interest taken.  Those funds are immediately available to the former property owner.  They are also free to challenge the government’s valuation and ask a jury to award them more money if they can prove the property had a higher value at the time of taking.<br><br>
  	<div id="section_faq">
    <h3>What is eminent domain?</h3>
    <div>Eminent domain is the power of some government entities (and a handful of private parties) to take private property for public use and is derived from the US Constitution and federal and state laws.</div>
    
    <h3>What should I do if I think my land is going to be taken?</h3>
    <div>The first thing you should do is consult with an attorney who can advise you of the process and what to expect.  If you retain counsel, the government will correspond and negotiate directly with your lawyer.</div>
    
    <h3>What am I entitled to receive when the government takes my property?</h3>
    <div>You are entitled just compensation, the fair market value of your property at the time of its taking.  Fair market value is generally considered the price for which a willing seller would sell and a willing buyer would pay for the property.</div>
    
    <h3>Is the government required to give me a fair price for my property?</h3>
    <div>Yes.  The government is required to pay fair market value for your property at the time of taking.  However, determining fair market value is not an exact science and the government, just like anyone else, will try to acquire it for the best (lowest) price possible.</div>
    
    <h3>How is my property’s fair market value determined and what factors are considered?</h3>
    <div>The government will hire one or more appraisers to determine the best and highest use for the property, regardless of its current use.  He or she will research recent sales of similar properties in the surrounding area for comparable prices and may also consider unique conditions of the property, such as recently constructed buildings or dilapidated structures that will need to be demolished before the property can be put to its best use.  Zoning and permissible uses for the property, its access to roadways and thoroughfares, and any other conditions that can affect the property’s value will also be considered.</div>
    
    <h3>What if I don't want to accept the government's offer for my property?</h3>
    <div>In most instances, the government is willing to negotiate on its initial price.  You should retain a lawyer well-versed in condemnation law—who knows how to scrutinize appraisal reports and evaluate the factors that influenced the appraiser’s valuation—to negotiate a fair amount.  In some instances, your lawyer may recommend hiring your own appraiser to provide another analysis.  If you aren’t able to agree on a fair price through those negotiations, the government may ultimately condemn the property and you will be entitled to a jury’s determination of the fair market value of the property.</div>
    
    <h3>What if I don't want to give up my property?</h3>
    <div>Unfortunately, if the government takes your property via eminent domain, your options are extremely limited, except in rare instances.  If you believe the government is taking your property for some reason other than a legitimate public use, you should consult an attorney as soon as possible.</div>
    
    <h3>Can eminent domain be used to take my property and give it to another private party?</h3>
    <div>This scenario is not supposed to happen, but sometimes does.  If you believe your property is going to be taken for any reason other than public use, you should contact a qualified lawyer to evaluate your case and advise you of your rights.</div>
    
