  <div id="summary">
    <div class="inner_wrapper"> Trust and estate, or fiduciary, litigation often and unfortunately centers on disputes between family members. <br>
      <br>
      In the context of a trust, the disputes most often center on a trustee’s alleged mismanagement of trust assets.  Causes of action include negligence, gross negligence, and breach of fiduciary duty.  Trey has represented beneficiaries and individual and corporate trustees in lawsuits of this ilk.<br>
      <br>
      In the context of a will, the dispute typically centers on the accusation that someone exerted undue influence, duress, or fraud upon the decedent in order to improperly acquire title to property passing through the estate.  Challenges to the validity of a will are called caveat proceedings and Trey successfully defended an unwarranted and meritless attack on the validity of a will in a jury trial. <br>
      <br>
      Trey spoke at the North Carolina Bar Association on the topic of <a href="http://www.lindleylawoffice.com/docs/CLE%20PRESENTATION%20MANUSCRIPT.pdf">Breach of Fiduciary Duty: Causes of Action and Defenses</a>.<br>
      <br>
     Trey also presented an interactive webinar for the North Carolina Bar Association titled<a href="http://www.lindleylawoffice.com/docs/Dead%20Man's%20Statute%20CLE.pdf">Alive and Well: The North Carolina Dead Man’s Statute</a>.<br><br>
      <a href="http://www.ncga.state.nc.us/gascripts/statutes/StatutesTOC.pl?Chapter=0036C">The North Carolina Uniform Trust Code (Chapter 36C of the North Carolina General Statutes)</a> regulates the creation, modification, validity, and termination of trusts as well as the fiduciary duties and responsibilities of trustees, including the grounds for their liability and removal.<br>
      <br>
      <div id="36c_faq">
        <h3>What are a trustee’s duties?</h3>
        <div>A trustee typically has the following duties:<br>
          <br>
          <ul>
            <li><em>duty of loyalty</em> to the beneficiaries such that he or she not engage in self-dealing;</li>
            <li><em>duty of care</em> to the trust property to collect and protect it, earmark it, and to not commingle it with his or her own property;</li>
            <li><em>duty to delegate</em> to others the doing of acts which he or she should delegate to someone of greater expertise;</li>
            <li><em>duty of impartiality</em> as to all beneficiaries; and</li>
            <li><em>duty to inform</em> and account to trust beneficiaries by making relevant disclosures.</li>
          </ul>
        </div>
        <h3>What are the most common causes of litigation over trusts?</h3>
        <div>The most common causes of litigation regarding trusts are: <br>
          <br>
          <ul>
            <li> one or more interested parties believes the trust instrument is <em>invalid</em>;</li>
            <li>the beneficiaries disagree with the trustee as to how the trust is being <em>administered </em>and whether it is being <em>managed</em> according to the trust instrument;</li>
            <li><em>changed circumstances</em> the settlor did not reasonably anticipate warrant a modification of the trust; and</li>
            <li> the trustee is accused of <em>breaching his or her fiduciary duties</em>.<br>
              <br>
            </li>
          </ul>
        </div>
        <h3>What are the bases for filing a lawsuit to challenge the validity of a trust or how it is being administered?</h3>
        <div>While there is no exhaustive list of reasons for which a lawsuit regarding a trust may be brought, the most common reasons are:<br>
          <br>
          <ul>
            <li><em>undue influence</em> of a beneficiary;</li>
            <li> the person who created the trust (the settlor) did so while under <em>duress</em>;</li>
            <li><em>lack of mental capacity</em> to create the trust;</li>
            <li>the settlor was <em>mistaken</em> in some material fashion;</li>
            <li>the settlor’s creation of the trust was the product of <em>fraud</em>;</li>
            <li>the trust in question is the product of a <em>forgery</em>; and</li>
            <li><em>breach of fiduciary</em> or <em>other duties</em> of the trustee.<br>
              <br>
            </li>
          </ul>
        </div>
        <h3>What can a trustee do to protect himself or herself from claims by the beneficiaries?</h3>
        <div>The trustee can request that the settlor create the trust such that the trustee is excluded from personal liability and/or will be indemnified out of the trust assets for claims made arising out of or relating to the trust.  Even so, the court may not enforce such provisions in the case, for example, of gross negligence or fraud.  A prudent trustee would be wise to secure insurance policy for claims made related to the management of the trust.</div>
      </div><br><br>
      <strong>The validity of the creation and revocation of wills is governed by <a href="http://www.ncga.state.nc.us/gascripts/Statutes/StatutesTOC.pl?Chapter=0031">Chapter 31 of the North Carolina General Statutes</a>.</strong><br><br>
      Challenges to the validity of a will, known as caveat proceedings, are also governed by this Chapter.<br>
      <br>
      <div id="c31_faq">
        <h3>Who is a testator?</h3>
        <div>A testator the person who creates a will.</div>
        <h3>Who is a beneficiary?</h3>
        <div>A beneficiary is a person or entity entitled to a bequest under a will.</div>
        <h3>Who is an heir?</h3>
        <div>An heir is someone who would be entitled to inherit from a deceased relative, when there is no will directing otherwise.</div>
        <h3>How is a will created?</h3>
        <div>There are several ways a valid will can be created, but the two most predominant are attested written wills (signed by the testator and two competent witnesses) and holographic wills (written entirely in the hand of and signed by the testator).  To ensure accurate preparation, effectuation of intent, and due consideration of tax consequences, an attorney should always be consulted and involved in estate planning matters.</div>
        <h3>Can I cancel or revoke my will?</h3>
        <div>Yes.  A valid, written will can be revoked one of two ways: (1) by<em> replacing</em> it with another valid will or (2) by being <em>destroyed</em> by the testator (or another person in the testator’s presence and at his/her direction) with the intent and purpose of revocation.<br>
          <br>
          If the testator divorces after the will is executed, the former spouse will not be entitled to any share of the estate unless the will specifically provides otherwise.</div>
         <h3> What happens if I die without a will or my will is determined to be invalid?</h3>
         <div>
          Property of a decedent without a valid will is distributed according to the <a href="http://www.ncga.state.nc.us/gascripts/statutes/StatutesTOC.pl?Chapter=0029">Intestate Succession</a> laws.  If the person is married with children, it will generally be divided among them, though not in equal shares.</div>
        <h3>My father passed away and my brother filed a will in which he gets everything.  I know my dad would never sign anything like that if he was in his right mind.  What can I do?</h3>
        <div>The first thing you should do is consult with a lawyer to determine if you have legitimate grounds to file a caveat proceeding.</div>
        <h3>What is a caveat proceeding?</h3>
        <div>A caveat proceeding is a legal challenge to a will’s validity; the challenger, or caveator, is entitled to have a jury trial to decide whether the challenged will, or any other purported will, is valid. </div>
        <h3>Who can file a caveat proceeding?</h3>
        <div>Any person who would be entitled to some property under the will in question or those interested in the estate may file a caveat proceeding.  To be “interested in the estate” means that the person is detrimentally affected by the will in question.  Those said to be “interested in the estate” includes heirs, next of kin, and anyone who would have inherited under a previous or later will, if valid and not otherwise revoked or replaced.</div>
        <h3>What are the bases for bringing a caveat proceeding?</h3>
        <div>While there is no exhaustive list of reasons for which a caveat proceeding may be brought, the most common reasons are:<br>
          <br>
          <ul>
            <li> <em>undue influence</em> of a beneficiary</li>
            <li>the deceased person (testator) executed the will while under <em>duress</em></li>
            <li>lack of “<em>testamentary capacity</em>” (lack of mental capacity to make a will) of the testator</li>
            <li><em>revocation</em> of the will in question</li>
            <li>the testator was <em>mistaken</em> as to what he was signing</li>
            <li>the testator’s execution of the will was the product of <em>fraud</em></li>
            <li>the will in question is a <em>forgery.</em><br>
              <br>
            </li>
          </ul>
        </div>
        <h3>When can a caveat proceeding be filed?</h3>
        <div>A caveat proceeding can be initiated within three years after the will has been filed (“probated”).</div>
        <h3>Who has the burden of proving the will is valid or invalid?</h3>
        <div>Those who submitted the will for probate (the propounders) are required to convince the jury it was executed with the proper formalities required by law.  If that burden is met, then the caveators must prove by the greater weight of the evidence that the disputed will is invalid for some other reason.</div>
        <h3>Can I recover my attorneys’ fees and costs if I win?</h3>
        <div>Yes.  The judge has the discretion to order the reasonable costs and attorneys’ fees be paid by one or more parties.  Assuming both sides’ arguments have merit, though, the attorneys’ fees are often taxed against the decedent’s estate.</div>
      </div><br><br>
      <a href="http://www.ncga.state.nc.us/gascripts/statutes/StatutesTOC.pl?Chapter=0028A">Probate of Wills and Administration of Estates: Chapter 28A of the North Carolina General Statutes</a> governs the administration of decedents’ estates, including how estate assets are managed, by whom, and how creditors’ claims are paid.  A great explanation of estate procedures can be found <a href="http://www.nccourts.org/forms/documents/735.pdf">here</a> while helpful forms can be found <a href="http://forms.lp.findlaw.com/states/nce_1.html">here</a>.   As with the creation of estate planning documents, the probatting and administrating of Estates should be accomplished with the assistance of qualified counsel.<br>
      <br>
      <div id="28a_faq">
        <h3>What is an executor and how are they appointed?</h3>
        <div>An executor is the person designated by the testator to administer the estate and is often a trusted relative or lawyer.  Upon probating a will, the clerk must approve the appointment of an executor so that he or she can legally perform his or her duties.  The Clerk of Court will issue Letters Testamentary for that purpose.</div>
        <h3>What are an executor’s duties?</h3>
        <div>An executor has three basic duties: (1) to <em>determine, locate</em>, and <em>assemble</em> the assets of the estate; (2) to determine the lawful <em>debts</em> of the estate and pay them; and (3) to <em>distribute</em> the remaining property to those entitled according to the will.</div>
        <h3>What happens to my property if I die without a will?</h3>
        <div>When a person dies without a will, he or she is said to have died intestate, and his or her property passes as provided by <a href="http://www.ncga.state.nc.us/gascripts/statutes/StatutesTOC.pl?Chapter=0029">North Carolina’s Intestate Succession</a> laws.</div>
        <h3>What is an administrator of an estate?</h3>
        <div>An administrator is someone appointed to handle the estate of someone who dies intestate, or without a will.  Upon filing the death certificate and an Application for Letters of Administration, the Clerk of Court will issue Letters of Administration, thereby giving the administrator legal authority to manage the estate assets.</div>
        <h3>Will all of my property pass through my estate when I die?</h3>
        <div>Not necessarily.  Real property and bank accounts owned as a “joint tenancy with right of survivorship” or as a “tenancy by the entirety” pass directly to the named survivor.  The same is true for life insurance proceeds or 401(k) accounts when the named beneficiary is someone other than the estate.  Personal property, stocks, and other property may be held in a trust established during the testator’s lifetime and thereby also avoid the probate process.</div>
