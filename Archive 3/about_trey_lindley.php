      <h2 style="width: 100%; font-family: 'Roboto-Slab', sans-serif;" >Biography</h2>
<p>Trey’s family relocated from his birthplace, Richmond, VA, to Charlotte, NC shortly after his sixth birthday. Although he likes to tell people he decided to become a lawyer at age twelve, the inspiration probably sparked much earlier. In furtherance of that goal, he pursued activities intended to hone his skills as an orator and rhetorician.</p><p>Trey was Co-Captain of his high school’s debate team, and he typically placed in every tournament in which he competed, winning first place one year at the state championship tournament.</p><p>After graduating from high school in Charlotte, Trey attended the University of North Carolina at Chapel Hill. Still focused on attending law school and becoming a litigator, he pursued degrees in philosophy (with a focus on the ethicists) and political science. Despite majoring in two of the more rigorous disciplines, he graduated with Highest Distinction (ranking in the top three percent of (3%) of his class) and was inducted as a member of Phi Beta Kappa. During college he served as a member of Student Congress and the Undergraduate Honor Court.</p><p>Trey spent the year following college in Washington, D.C. where he worked as a paralegal for the largest D.C.-based law firm before returning to Chapel Hill for law school. During law school, Trey served on the Attorney General&#8217s Staff for the law school’s Honor Court and was a member of the North Carolina Law Review. Upon graduating with Honors and passing the bar in 2003, Trey returned to Charlotte where he has practiced civil litigation ever since. For the past five years in a row, Trey was named a Rising Star in Business Litigation by North Carolina Super Lawyer’s Magazine. In 2017, he was named a Rising Star in Trust and Estate Litigation. These honors are awarded to fewer than two and one-half percent (2.5%) of all lawyers across the State of North Carolina. In February 2014, the Charlotte Business Journal named Trey to their Top 40 Under 40 list which recognizes the top business leaders under the age of 40 in the Charlotte region.</p> <p>With over a decade of experience and after becoming a partner in another firm, Trey organized Lindley Law, PLLC in July 2014. Trey’s office is conveniently located at 326 W. 10th Street, Charlotte NC 28202.</p></div><!-- REP --><div id="rep_work" class="section" style="float: right; color:#fff;"><div class="light_text_box" style="float: right; margin-right: 5%" data-anchor-target="#rep_work">
