      <h2 style="width: 100%; font-family: 'Roboto-Slab', sans-serif;" >Biography</h2>
      
Liz grew up in Hickory, NC where she was raised with her three younger siblings. After graduating from St. Stephens High School, she went to the University of North Carolina at Chapel Hill where she majored in Peace, War, and Defense as well as Political Science. <br><br>

Immediately following undergrad, she enrolled in the UNC School of Law where she participated in her school’s Pro Bono Program, including working as a coordinator for the Volunteer Income Tax Assistance program and traveling to New Orleans for a week to assist victims of Hurricane Katrina with the unique property rights system of Louisiana. She also participated in the university’s Immigration and Human Rights Policy Clinic where she assisted real clients with the assistance of a supervising attorney. <br><br>

Following her graduation and passing the North Carolina Bar Exam, she worked as a post-conviction litigation intern with the North Carolina Center on Actual Innocence. In her time there, she worked on the case of Gregory Taylor, the first man declared factually innocent in North Carolina by a three judge panel after being referred by the North Carolina Innocence Inquiry Commission. During this time, she was also studying for the California Bar Exam, which she passed in 2010. <br><br>

Additionally, she worked as a contract attorney with two different eDiscovery companies in Charlotte and was ultimately hired permanently as a staff attorney with DiscoverReady. Throughout her years in eDiscovery, she provided comprehensive document review and quality control for Fortune 500 companies and AMLAW 200 firms focused on multimillion dollar litigation related to mortgage backed securities, patent law, employment law, environmental law, and federal investigations.
<br><br>
Outside of the office, Liz enjoys cheering for the Carolina Panthers and UNC Tar Heels, traveling, exploring Charlotte’s restaurant scene, and crossword puzzles. She also enjoys spending time with her friends, family, and Basset Hound, Maggie.
<br><br>
Liz joined the Lindley Law team in March of 2016 as the Marketing Director and Office Manager. With her varied background in the law, she adds a unique perspective to legal marketing and the management of a law office. 

