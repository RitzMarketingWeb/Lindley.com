      <h2 style="width: 100%; font-family: 'Roboto-Slab', sans-serif;" >Biography</h2>
      After graduating from Cardinal Gibbons High School in Raleigh, North Carolina, Satie attended Wake Forest University. During college she joined several student organizations, spent a semester studying in Venice, Italy, and participated in a service trip to Vietnam, after which she was granted membership in the Pro Humanitate community service honor society. She graduated cum laude from Wake Forest University, with a bachelor of arts in Political Science and a minor in Mass Communication.<br><br>
Having always had an interest in law, Satie returned to her hometown of Chapel Hill, North Carolina, where she attended the University of North Carolina School of Law. During law school, she served as a board member for the First Amendment Law Review, Women in Law, and Student Bar Association. Satie worked as an intern for Judge William A. Webb, federal magistrate judge for the U.S. District Court for the Eastern District of North Carolina, had a range of internships and externships throughout North Carolina and Washington D.C., and completed over 100 hours of pro bono work during her time at UNC. In the fall of her third year, Satie was accepted into the Juvenile Justice Clinic program where she counseled, advised, and advocated for juveniles in court proceedings.<br><br>
 
Outside of the practice of law, Satie enjoys traveling, playing tennis, shooting clay, and spending time with family and friends.<br><br>
Satie joined the Lindley Law team in 2015. Since moving to Charlotte, Satie has immersed herself in the community.  She currently serves on the board for the Young Affiliates of the Mint, and is an active member of the Junior League of Charlotte and United Way of the Central Carolinas LINC. With a variety of experiences in the legal field, she adds a wealth of unique knowledge to Lindley Law. Satie is licensed to practice law in North Carolina.</div>
