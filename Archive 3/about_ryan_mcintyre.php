      <br><br><h2 style="width: 100%; font-family: 'Roboto-Slab', sans-serif;" >Biography</h2><br>
      
      Ryan grew up on the eastern end of Long Island with his two older siblings. After graduating from Chaminade High School, he attended Cornell University in Ithaca, NY. At Cornell, Ryan excelled both athletically and academically as a full time student athlete. Athletically, he competed on the Varsity Lightweight Rowing Team, winning the Second Varsity Lightweight Ivy League Championship his senior year. Academically, he graduated with a bachelor of science in Industrial & Labor Relations and a minor in Economics.<br><br>
      
Inspired by his father’s legal career, Ryan enrolled in Wake Forest School of Law immediately following his graduation from Cornell. At Wake, Ryan developed his litigation skills through membership on the Moot Court Board. He competed on the Jessup International Moot Court Team and later served as Chief Justice of the Moot Court Board.<br>
Ryan also gained valuable experience through participation in multiple law school clinics. He tried misdemeanor criminal cases alongside Forsyth County Assistant District Attorneys in the Wake Forest Litigation Clinic. He helped small business owners file as corporate entities and draft various contracts in the Community Law & Business Clinic. Additionally, Ryan worked in Charlotte as a judicial intern for Judge Richard L. Voorhees of the U.S. District Court for the Western District of North Carolina.<br><br>

Outside of the office, Ryan enjoys mountain biking, hiking, and all things outdoors. He is also a big fan of the New York Rangers, Miami Dolphins, and AFC Wimbledon.
Ryan joined the Lindley Law team in October of 2016 as an Associate Attorney. His diverse educational background and variety of legal experiences allow him to solve a wide range of legal issues. Ryan is licensed to practice law in North Carolina.<br><br>

